package CustomVehicle;

public class Vehicle {
    int num_of_wheels;
    float mileage;
    float horsepower;

    public Vehicle(int num_of_wheels, float mileage, float horsepower) {
        this.num_of_wheels = num_of_wheels;
        this.mileage = mileage;
        this.horsepower = horsepower;
    }
}
